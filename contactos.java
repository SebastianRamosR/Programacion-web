
public class contactos {
    String name;
    String numner;
    String gmail;
    public contactos(String name, String numner, String gmail) {
        this.name = name;
        this.numner = numner;
        this.gmail = gmail;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getNumner() {
        return numner;
    }
    public void setNumner(String numner) {
        this.numner = numner;
    }
    public String getGmail() {
        return gmail;
    }
    public void setGmail(String gmail) {
        this.gmail = gmail;
    }
    
}
